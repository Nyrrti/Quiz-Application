// create one quiz instance
const quiz = new Quiz(); 
const renderQuiz = new RenderQuiz();


// change the json to objects so I work with it
quiz.loadData(questions);

const nextButton = document.getElementById("nextButton");
const previousButton = document.getElementById("previousButton");    
const finishButton = document.getElementById("finishButton");

//Next Button
nextButton.addEventListener("click", function() {
    // Allowed to move when answer selected or timer runs out
    const canProceed = quiz.checkIfAnswered(quiz.currentQuestion);
    if (!canProceed) {
        renderQuiz.displayError();
        return;
    }

    const nextQuestion = quiz.nextQuestion();
    if (!nextQuestion.isAnswered) {
        renderQuiz.startTimer(nextQuestion);
    }

    // renderQuiz.startTimer(quiz.currentQuestion);
    // Clear the current trivia
    renderQuiz.clearTrivia();
    // Move the index forward and returns the next Question object
    renderQuiz.renderQuestion(nextQuestion);
    // Display finishbutton at end of array
    renderQuiz.displayFinishButton(quiz.currentIndex, quiz.length());
    // Display the current index of total questions
    renderQuiz.displayIndex(quiz.currentIndex, quiz.length());
    // Hide previous at start, hide next at end
    renderQuiz.updateButtonVisibility(quiz.currentIndex, quiz.length());
});

//Previous Button
previousButton.addEventListener("click", function() {
    // Allowed to move when answer selected or timer runs out
    const canProceed = quiz.checkIfAnswered(quiz.currentQuestion);
    if (!canProceed) {
        renderQuiz.displayError();
        return;
    }

    // Move back to the previous question and save the question data.
    const question = quiz.previousQuestion();

    if(question.isExpired) {
        renderQuiz.displayIrisOverlay;
    }

    // Display the lockedletters saved in array
    renderQuiz.displayLockedLetters(question.lockedLetters);
    // Clear the current trivia
    renderQuiz.clearTrivia();
    // Render the previous question again
    renderQuiz.renderQuestion(question);
    // Display the current index of total questions
    renderQuiz.displayIndex(quiz.currentIndex, quiz.length());
    // Hide previous at start, hide next at end
    renderQuiz.updateButtonVisibility(quiz.currentIndex, quiz.length());
});

finishButton.addEventListener("click", function(){

     // Allowed to move when answer selected or timer runs out
    const canProceed = quiz.checkIfAnswered(quiz.currentQuestion);
    if (!canProceed) {
        renderQuiz.displayError();
        return;
    }
    console.log("Finish it!");
    quiz.storeQuizData();
    window.location.href = "./assets/html/quizEnd.html";
});

// Start and Restart Quiz
startButton.addEventListener("click", function() {
    startButton.textContent = "Restart Quiz";

    renderQuiz.startTimer(quiz.currentQuestion);
    renderQuiz.clearTrivia();
    renderQuiz.renderQuestion(quiz.restart()); 
    renderQuiz.displayIndex(quiz.currentIndex, quiz.length());
    renderQuiz.createPreviousButton();
    renderQuiz.createNextButton();
    renderQuiz.updateButtonVisibility(quiz.currentIndex, quiz.length());
});


		
