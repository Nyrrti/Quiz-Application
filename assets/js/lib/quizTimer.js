class QuizTimer {
    /** @type {number} */
    #totalBlocks;
    /** @type {string[]} */
    #lockedLetters;
    /** @type {number} */
    #currentBlock;
    /** @type {null} */
    #blockTimer;
    /** @type {null} */
    #TimeOutTimer;
    /** @type {null} */
    #onCompleteCallback;

    constructor() {
        this.#totalBlocks = 7;
        this.#lockedLetters = [];
        this.#currentBlock = 0;
        this.#blockTimer = null;
        this.#TimeOutTimer = null;
        this.#onCompleteCallback = null;
    }

    /**
     * 
     * @returns Returns a random letter
     */
    #getRandomLetter() {
        let letters = 'AaBbCcDdEeFfGgHIJKLMNOPQRSTUVWXYZ'.split('');
        return letters[Math.floor(Math.random() * letters.length)];
    }

    buildBlocks() {
        const container = document.getElementById("timerArea");
        container.innerHTML = "";

        for (let i = 0; i < this.#totalBlocks; i++) {
            let block = document.createElement("h4");
            block.id = "block-" + i;
            block.className = 'glyph-block glyphs';
            container.appendChild(block);
        }
    }

    /**
     * 
     * @param {HTMLElement} htmlElement The element you want to apply the class to
     */
    displayOrange(htmlElement) {
        htmlElement.classList.add("orange");
    }

    // Color the block orange after the timer gets cut off
    lockRevealedBlocks() {
        for (let i = 0; i < this.#lockedLetters.length; i++) {
            const block = document.getElementById("block-" + i);
            if (block) this.displayOrange(block);
        }
    }

    /** Reveals a single block by index, gets a random letter, then locks it after 4 seconds.
     * 
     * @param {number} index - The index of the block to reveal (0-6)
     */
    revealBlock(index) {
        this.#currentBlock = index;
        const block = document.getElementById("block-" + index);
        const letter = this.#getRandomLetter();
        this.#lockedLetters.push(letter);
    
        // Shuffle letter timer for the block
        this.#blockTimer = setInterval(() => {
            block.textContent = this.#getRandomLetter();
        }, 100);

        // Auto lock after 2 secs
        this.#TimeOutTimer = setTimeout(() => {

            // Stop the letter shuffle
            clearInterval(this.#blockTimer);

            // Render on screen
            block.textContent = letter;
            // Add Orange color
            this.displayOrange(block);

            // Add 1 to currentBlock index
            this.#currentBlock++;

            // If the currentBlock is lower then totalBlocks add another Block
            if (this.#currentBlock < this.#totalBlocks) {
                this.revealBlock(this.#currentBlock);
            }
            else {
                // When all blocks filled, time's up and lock answers
                this.#onCompleteCallback();
                this.lockRevealedBlocks();
            }
        }, 2000);
    }

    
    /** 
     * 
     * @param {function():void} callBack 
     */
    onExpired(callBack) {
          this.#onCompleteCallback = callBack;
    }

    // Start Timer
    start() {
        // Build the div with blocks
        this.buildBlocks();
        // Add 1 block with shuffled letters
        this.revealBlock(0);
    }

    startStatic() {
        // Build the div with blocks
        this.buildBlocks();
    }

    stopTimer() {
        clearInterval(this.#blockTimer);
        clearTimeout(this.#TimeOutTimer);
    }

    // Clean timers, data, references, DOM
    reset() {
        clearInterval(this.#blockTimer);
        clearTimeout(this.#TimeOutTimer);
        this.#lockedLetters = [];
        this.#currentBlock = 0;
        this.#blockTimer = null;
        this.#TimeOutTimer = null;
        const container = document.getElementById("timerArea");
        container.innerHTML = "";
    }
     
    /** Amount of timer blocks
     * @returns {number} 
     */
    get totalBlocks() {
        return this.#totalBlocks;
    } 

    /** The locked letters generated by the random letter shuffle and get saved in the array
     * @returns {string[]} 
     */
    get lockedLetters() {
        return this.#lockedLetters;
    } 
}
